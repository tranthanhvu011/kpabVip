    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $query = Classes::query();
        if($request->has('s')){
            $s = $request->s;
            $query->where('name', 'like', "%{$s}%");
        }
        $items = $query->paginate(config('constants.items_per_page'));
        return view('class.index', compact('items'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $teachers = User::where('role_id', Role::TEACHER_ID)->get()->pluck('name', 'id');
        $students = User::where('role_id', Role::STUDENT_ID)->get()->pluck('name', 'id');
        return view('class.form', compact('teachers', 'students'));
    }

